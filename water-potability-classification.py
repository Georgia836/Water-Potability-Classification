ph_df = df.dropna(subset=["ph"]).copy()

def ph_category(ph):
    if ph < 6.5:
        return "pH < 6.5"
    elif ph <= 8.5:
        return "6.5 <= pH <= 8.5"
    else:
        return "pH > 8.5"

ph_df["pH_Category"] = ph_df["ph"].apply(ph_category)

ph_potability_percentages = pd.crosstab(
    ph_df["pH_Category"],
    ph_df["Potability"],
    normalize="index"
) * 100

ph_potability_percentages.columns = ["Not Potable (%)", "Potable (%)"]
display(ph_potability_percentages)

ph_potability_counts = pd.crosstab(ph_df["pH_Category"], ph_df["Potability"])
display(ph_potability_counts)

