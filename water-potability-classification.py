df_filled = df.copy()

for col in df_filled.columns:
    if df_filled[col].isnull().sum() > 0:
        df_filled[col] = df_filled[col].fillna(df_filled[col].mean())

display(df_filled.isnull().sum())

X = df_filled.drop(columns=["Potability"]).values
y = df_filled["Potability"].values

feature_names = df_filled.drop(columns=["Potability"]).columns

X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.30,
    random_state=0
)

print("Training examples:", X_train.shape[0])
print("Test examples:", X_test.shape[0])

dt = DecisionTreeClassifier(random_state=0)
dt.fit(X_train, y_train)

y_train_pred = dt.predict(X_train)
y_test_pred = dt.predict(X_test)

print("Decision Tree Train Accuracy:", accuracy_score(y_train, y_train_pred))
print("Decision Tree Test Accuracy:", accuracy_score(y_test, y_test_pred))
print(classification_report(y_test, y_test_pred))

param_grid_dt = {
    "criterion": ["gini", "entropy"],
    "max_depth": [None, 3, 5],
    "min_samples_split": [2, 5],
    "min_samples_leaf": [1, 2],
    "max_features": [None, "sqrt"],
    "ccp_alpha": [0, 0.01]
}

results_dt = []

for params in ParameterGrid(param_grid_dt):
    model = DecisionTreeClassifier(random_state=0, **params)
    model.fit(X_train, y_train)

    train_pred = model.predict(X_train)
    test_pred = model.predict(X_test)

    results_dt.append({
        **params,
        "train_acc": accuracy_score(y_train, train_pred),
        "test_acc": accuracy_score(y_test, test_pred)
    })

results_dt_df = pd.DataFrame(results_dt).sort_values(by="test_acc", ascending=False)
display(results_dt_df)

def clean_params(params):
    params = dict(params)

    if "max_depth" in params:
        if pd.isna(params["max_depth"]):
            params["max_depth"] = None
        else:
            params["max_depth"] = int(params["max_depth"])

    if "min_samples_split" in params:
        params["min_samples_split"] = int(params["min_samples_split"])

    if "min_samples_leaf" in params:
        params["min_samples_leaf"] = int(params["min_samples_leaf"])

    if "n_estimators" in params:
        params["n_estimators"] = int(params["n_estimators"])

    if "ccp_alpha" in params:
        params["ccp_alpha"] = float(params["ccp_alpha"])

    return params

best_depth3 = results_dt_df[results_dt_df["max_depth"] == 3].sort_values(
    by="test_acc",
    ascending=False
).iloc[0]

display(best_depth3)

best_depth3_params = best_depth3.drop(["train_acc", "test_acc"]).to_dict()
best_depth3_params = clean_params(best_depth3_params)

best_depth3_tree = DecisionTreeClassifier(
    random_state=0,
    **best_depth3_params
)

best_depth3_tree.fit(X_train, y_train)

plt.figure(figsize=(24, 12))
